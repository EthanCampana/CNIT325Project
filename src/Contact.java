
public abstract class Contact {
    protected String PhoneNumber;

    public String getPhoneNumber() {
        return PhoneNumber;
    }

    public void setPhoneNumber(String PhoneNumber) {
        this.PhoneNumber = PhoneNumber;
    }

    public String getOperatingHR() {
        return OperatingHR;
    }

    public void setOperatingHR(String OperatingHR) {
        this.OperatingHR = OperatingHR;
    }
    protected String OperatingHR;
}
